package com.string1;

//5.	Write a Java program to concatenate two strings.
public class Q5ConcatenateTwoStrings {

	public static void main(String[] args) {
		
		String s1 = "Good";
		String s2 = "Morning";
		
		System.out.println(s1.concat(s2)); // concat()
		System.out.println(s1+s2);         // +

	}

}
