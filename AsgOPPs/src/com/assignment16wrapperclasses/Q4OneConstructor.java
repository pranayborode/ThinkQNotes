//4.	Which wrapper class has only 1 constructor
//   Ans - the Character wrapper class in Java has only one constructor. 
//         This constructor takes a char value as an argument:

public class Q4OneConstructor {

	public static void main(String[] args) {

		Character ch = new Character((char) 'A');

		System.out.println(ch);

	}

}
