package com.assignment16wrapperclasses;

//15.	Show one example of auto boxing for each wrapper class.

public class Q15AutoBoxing {

	public static void main(String[] args) {
		
		Byte b1 = 12;
		System.out.println("Byte value : "+b1);
		
		Short s1 = 120;
		System.out.println("Short value : "+s1);
		
		Integer i1 = 1234;
		System.out.println("Integer value : "+i1);
		
		Long l1 = 1234567890L; 
		System.out.println("Long value : "+l1);
		
		Float f1 = 12.12f;
		System.out.println("Float value : "+f1);
		
		Double d1 = 1234.1234;
		System.out.println("Double value : "+d1);
		
		Character ch = 'A';
		System.out.println("Character value : "+ch);

		Boolean bol = true;
		System.out.println("Boolean value : "+bol);
		
	}

}
