//11. From scanner In loop (for 2 iterations) read Student data create new object each time assign values and 
//    print student values in console.

public class Q11Student1 {

	private int rollNo;
	private String StdName;
	private int age;

	public int getRollNo() {
		return rollNo;
	}

	public void setRollNo(int rollNo) {
		this.rollNo = rollNo;
	}

	public String getStdName() {
		return StdName;
	}

	public void setStdName(String stdName) {
		StdName = stdName;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
