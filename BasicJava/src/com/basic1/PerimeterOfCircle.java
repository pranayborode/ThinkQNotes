package com.basic1;

public class PerimeterOfCircle {

	public static void main(String[] args) {

		double r = 5;
		double pi = 3.14;

		double area = pi * r * r;

		double perimeter = 2 * pi * r;
		
		System.out.println("Area of Circle is "+area);

		System.out.println("Perimeter of Circle is " + perimeter);

	}

}
