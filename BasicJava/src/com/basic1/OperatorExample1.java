package com.basic1;

public class OperatorExample1 {

	public static void main(String[] args) {
		
		int x = 10;
		
		System.out.println(x++); // 10
		System.out.println(x);   //11
		
		System.out.println(++x); //12
		System.out.println(x);   //12
		
		System.out.println(x--); //12 
		System.out.println(x);   //11
		
		System.out.println(--x); //10
		System.out.println(x);   //10

	}

}
