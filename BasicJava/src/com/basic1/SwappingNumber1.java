package com.basic1;

public class SwappingNumber1 {

	public static void main(String[] args) {
		
		
		int x = 10;
		int y = 20;
		int z ;
		
		System.out.println("Before : x ="+x+" y ="+y );
		
		z = x;
	    x = y;
	    y = z;
	    
	    System.out.println("After : x ="+x+ " y ="+y);
		
		
		
		

	}

}
