package com.basic2;

public class ArithmeticOperator {

	public static void main(String[] args) {
		
		int a = 20;
		int b = 10;
		
		int sum = a + b;
		int min = a - b;
		int mul = a * b;
		int div = a / b;
		int mod = a % b;
		
		System.out.println("a+b = "+sum);
		System.out.println("a-b = "+min);
		System.out.println("a*b = "+mul);
		System.out.println("a/b = "+div);
		System.out.println("a%b = "+mod);

	}

}
